package com.example.zerohunger;

public class User {
    public String firstName, lastName, email, indexNumber, referenceNumber, phoneNumber, hostel;

    public User(){

    }

    public User(String firstName, String lastName, String email, String indexNumber, String referenceNumber, String phoneNumber, String hostel){
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.indexNumber = indexNumber;
        this.referenceNumber = referenceNumber;
        this.phoneNumber = phoneNumber;
        this.hostel = hostel;

    }
}
